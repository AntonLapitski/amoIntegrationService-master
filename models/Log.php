<?php

namespace app\models;

use Yii;

/**
 * This is the model class for table "log".
 *
 * @property int $id
 * @property int $sid
 * @property int $config_id
 * @property string $event_sid
 * @property array $data
 *
 * @property Config $config
 */
class Log extends \yii\db\ActiveRecord
{
    /**
     * {@inheritdoc}
     */
    public static function tableName()
    {
        return 'log';
    }

    /**
     * {@inheritdoc}
     */
    public function rules()
    {
        return [
            [['config_id'], 'integer'],
            [['data'], 'safe'],
            [['event_sid', 'sid'], 'string', 'max' => 255],
            [['config_id'], 'exist', 'skipOnError' => true, 'targetClass' => Config::className(), 'targetAttribute' => ['config_id' => 'id']],
            [['sid'], 'unique'],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'config_id' => 'Config ID',
            'event_sid' => 'Event Sid',
            'contact' => 'Contact',
            'lead' => 'Lead',
            'customer' => 'Customer',
            'user' => 'User',
            'settings' => 'Settings',
        ];
    }

    /**
     * @param $insert
     * @return mixed
     */
    public function beforeSave($insert)
    {
        if($this->isNewRecord)
            $this->sid = 'LG'.md5($this->event_sid.time().rand(1000, 9999));

        return parent::beforeSave($insert); // TODO: Change the autogenerated stub
    }

    /**
     * Gets query for [[Config]].
     *
     * @return \yii\db\ActiveQuery
     */
    public function getConfig()
    {
        return $this->hasOne(Config::className(), ['id' => 'config_id']);
    }
}
